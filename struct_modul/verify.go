package structmodul

///////////////////////////////////////////////////////////////////////////////
// Структура: Verify
// Містить інформацію про один перевірений файл
///////////////////////////////////////////////////////////////////////////////

type Verify struct {
	Path string `json:"path"` // Повний шлях до файлу на диску
	Name string `json:"name"` // Ім’я файлу (без шляху)
	Hash string `json:"hash"` // SHA-256 хеш вмісту файлу
}
